#include "libft.h"

void	*ft_memset(void *b, int c, size_t len)
{
	char *ptr_b;

	ptr_b = b;
	while (len > 0)
	{
		*ptr_b = c;
		ptr_b++;
		len--;
	}
	return (b);
}
